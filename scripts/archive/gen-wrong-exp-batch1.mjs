// ─── Main ───

async function main() {
  console.log('=== Wrong Explanations Update (Batch 1) ===')
  console.log('Subjects: introduction, curriculum, transition, laws\n')

  const subjects = ['introduction', 'curriculum', 'transition', 'laws']
  const summary = {}
  let totalUpdated = 0

  for (const subject of subjects) {
    console.log(`--- ${subject} ---`)

    const { data: questions, error } = await supabase
      .from('quiz_questions')
      .select('id')
      .eq('subject', subject)
      .eq('type', 'multiple')
      .is('wrong_explanations', null)
      .order('id')

    if (error) {
      console.error(`  Fetch error: ${error.message}`)
      summary[subject] = 'ERROR'
      continue
    }

    let updatedCount = 0

    for (const q of questions) {
      const wrongExp = WRONG_EXPLANATIONS[q.id]
      if (!wrongExp) {
        console.error(`  SKIP: No pre-computed data for ${q.id}`)
        continue
      }

      const { error: updateError } = await supabase
        .from('quiz_questions')
        .update({ wrong_explanations: wrongExp })
        .eq('id', q.id)

      if (updateError) {
        console.error(`  UPDATE ERROR: ${q.id} - ${updateError.message}`)
        continue
      }

      updatedCount++
      console.log(`  Updated: ${q.id}`)
    }

    summary[subject] = updatedCount
    totalUpdated += updatedCount
    console.log(`  Subtotal: ${updatedCount}/${questions.length}\n`)
  }

  console.log('=== Summary ===')
  for (const [subject, count] of Object.entries(summary)) {
    console.log(`  ${subject}: ${count}`)
  }
  console.log(`  TOTAL: ${totalUpdated}`)

  // Verification
  console.log('\n=== Verification ===')
  for (const subject of subjects) {
    const { data: remaining } = await supabase
      .from('quiz_questions')
      .select('id')
      .eq('subject', subject)
      .eq('type', 'multiple')
      .is('wrong_explanations', null)

    const { data: total } = await supabase
      .from('quiz_questions')
      .select('id')
      .eq('subject', subject)
      .eq('type', 'multiple')

    const done = (total?.length || 0) - (remaining?.length || 0)
    console.log(`  ${subject}: ${done}/${total?.length || 0} have wrong_explanations (${remaining?.length || 0} remaining null)`)
  }
}

main().catch(console.error)
