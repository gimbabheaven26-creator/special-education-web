async function main() {
  const subjects = ['communication-disorder', 'hearing-impairment', 'physical-disability', 'visual-impairment']
  const results = {}

  for (const subject of subjects) {
    const { data: questions, error: fetchError } = await supabase
      .from('quiz_questions')
      .select('id')
      .eq('type', 'multiple')
      .eq('subject', subject)
      .is('wrong_explanations', null)
      .order('id')

    if (fetchError) {
      console.error(`Failed to fetch ${subject}:`, fetchError.message)
      results[subject] = { fetched: 0, updated: 0, errors: [] }
      continue
    }

    let updated = 0
    const errors = []

    for (const q of questions) {
      const we = wrongExplanations[q.id]
      if (!we) {
        errors.push(`No wrong_explanations data for ${q.id}`)
        continue
      }

      const { error: updateError } = await supabase
        .from('quiz_questions')
        .update({ wrong_explanations: we })
        .eq('id', q.id)

      if (updateError) {
        errors.push(`Failed to update ${q.id}: ${updateError.message}`)
      } else {
        updated++
      }
    }

    results[subject] = { fetched: questions.length, updated, errors }
    console.log(`${subject}: ${updated}/${questions.length} updated`)
    if (errors.length > 0) {
      errors.forEach(e => console.error(`  ERROR: ${e}`))
    }
  }

  console.log('\n=== Summary ===')
  let totalUpdated = 0
  for (const [subject, result] of Object.entries(results)) {
    console.log(`${subject}: ${result.updated}/${result.fetched} updated` +
      (result.errors.length > 0 ? ` (${result.errors.length} errors)` : ''))
    totalUpdated += result.updated
  }
  console.log(`Total: ${totalUpdated} questions updated`)
}

main().catch(err => {
  console.error('Script failed:', err)
  process.exit(1)
})
