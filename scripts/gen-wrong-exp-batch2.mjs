async function updateQuestion(id, wrongExplanations) {
  const { error } = await supabase
    .from('quiz_questions')
    .update({ wrong_explanations: wrongExplanations })
    .eq('id', id)

  if (error) {
    throw new Error(`Failed to update ${id}: ${error.message}`)
  }
}

async function processSubject(subject) {
  console.log(`\n--- ${subject} ---`)

  const { data: questions, error } = await supabase
    .from('quiz_questions')
    .select('id, answer')
    .eq('subject', subject)
    .eq('type', 'multiple')
    .is('wrong_explanations', null)

  if (error) {
    throw new Error(`Failed to fetch ${subject}: ${error.message}`)
  }

  if (!questions || questions.length === 0) {
    console.log('  No questions need wrong_explanations')
    return 0
  }

  console.log(`  ${questions.length} questions to process`)

  let updatedCount = 0
  let skippedCount = 0

  for (const q of questions) {
    const wrongExp = WRONG_EXPLANATIONS[q.id]
    if (!wrongExp) {
      console.error(`  WARNING: No pre-generated data for ${q.id}`)
      skippedCount++
      continue
    }

    // Validate: wrong_explanations should not include the correct answer index
    const correctIdx = q.answer
    if (wrongExp[correctIdx]) {
      console.error(`  WARNING: ${q.id} has explanation for correct answer index ${correctIdx}, removing`)
      const { [correctIdx]: _, ...rest } = wrongExp
      await updateQuestion(q.id, rest)
    } else {
      await updateQuestion(q.id, wrongExp)
    }

    updatedCount++
    console.log(`  Updated: ${q.id}`)
  }

  if (skippedCount > 0) {
    console.log(`  Skipped: ${skippedCount}`)
  }
  console.log(`  Total updated: ${updatedCount}/${questions.length}`)
  return updatedCount
}

async function main() {
  console.log('=== Wrong Explanations Generator (Batch 2) ===')
  console.log('Subjects:', SUBJECTS.join(', '))

  const summary = {}
  let totalUpdated = 0

  for (const subject of SUBJECTS) {
    try {
      const count = await processSubject(subject)
      summary[subject] = count
      totalUpdated += count
    } catch (err) {
      console.error(`ERROR processing ${subject}:`, err.message)
      summary[subject] = `ERROR: ${err.message}`
    }
  }

  console.log('\n=== Summary ===')
  for (const [subject, count] of Object.entries(summary)) {
    console.log(`  ${subject}: ${count}`)
  }
  console.log(`  TOTAL: ${totalUpdated}`)

  console.log('\n=== Verification ===')
  for (const subject of SUBJECTS) {
    const { data: remaining } = await supabase
      .from('quiz_questions')
      .select('id')
      .eq('subject', subject)
      .eq('type', 'multiple')
      .is('wrong_explanations', null)

    const { data: total } = await supabase
      .from('quiz_questions')
      .select('id')
      .eq('subject', subject)
      .eq('type', 'multiple')

    console.log(`  ${subject}: ${(total?.length || 0) - (remaining?.length || 0)}/${total?.length || 0} have wrong_explanations (${remaining?.length || 0} remaining)`)
  }
}

